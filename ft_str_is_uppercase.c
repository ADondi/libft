#include "libft.h"

int	ft_str_is_uppercase(char *s)
{
	char *p_s;

	p_s = s;
	while (*p_s)
	{
		if (*p_s <= 'A' || *p_s >= 'Z')
			return (0);
		p_s++;
	}
	return (1);
}

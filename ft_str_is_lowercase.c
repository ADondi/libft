#include "libft.h"

int	ft_str_is_lowercase(char *s)
{
	char *p_s;

	p_s = s;
	while (*p_s)
	{
		if (*p_s < 'a' || *p_s > 'z')
			return (0);
		p_s++;
	}
	return (1);
}

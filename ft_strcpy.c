#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int	i;

	i = 0;
	while (*src)
	{
		*dst = *src;
		src++;
		dst++;
		i++;
	}
	*dst = '\0';
	dst -= i;
	return (dst);
}
